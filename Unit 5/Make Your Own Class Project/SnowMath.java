public class SnowMath{
    public static int coalMouth(int num){
        return num * 2; //num is how many pieces of coal are used for the mouth
                        //each piece of coal is about 2 inches long
    }
    
    public static int noseLength(int n){
        return n - 1; //n is the length of the carrot used for the nose
                     //1 is subtracted so that the carrot can be pushed into the face far enough
    }
}
